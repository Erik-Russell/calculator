// operator functions

const add = function (a, b) {
    return a + b;
};

const subtract = function (a, b) {
    return a - b;
}

const multiply = function (a, b) {
    return a * b;
}

const divide = function (a, b) {
    return a / b;
}

const reciprocal = function (a) {
    return 1 / a;
}

const square = function (a) {
    return a ** 2;
}

const root = function (a) {
    return a ** (1 / 2);
}